package belajar1;

public class Main {
   // operator penugasan 
    public static void main(String[] args) {
        int a = 20;
        a += a;
        System.out.println(a);
        a-=20;
        System.out.println(a);
        a*=a;
        System.out.println(a);
        a/=5;
        System.out.println(a);
        a%=5;
        System.out.println(a+"\nKHAMSHAMNIDA:)");
    }
    
}
