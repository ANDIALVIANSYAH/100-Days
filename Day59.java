package Day100ofcoding6;

public class Day100ofcoding6 {
    
    public static void main(String[] args) {
        // Pak bayu ingin membuat program yang menyimpan nama-nama mahasiswa dengan nilai tertinggi.
        //Berikut adalah daftar nama mahasiswa dengan nilai tertinggi (Andi, Alvin, Jeno, Rizky, Abdi.) 
        //(simpan dalam array)
        String nama[] = {"Andi ","Alvin ","Jeno ","Rizky ","Abdi "};
        for(int i = 0; i < nama.length; i++){
            System.out.println(i + 1+" "+nama[i]);
        }
    }
    
}
