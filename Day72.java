package Day100ofcoding18;

public class Day100ofcoding18 {
    
    public static void main(String[] args) {
        // Panggil fungsi yang menggunakan while loop
        myWhileLoopFunction();
    }

    // Fungsi dengan while loop
    public static void myWhileLoopFunction() {
        int i = 1;

        // While loop untuk mencetak angka 1 hingga 5
        while (i <= 5) {
            System.out.println("Angka: " + i);
            i++;
        }
    }
}

    
