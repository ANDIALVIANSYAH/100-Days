package Day100ofcoding10;

public class Day100ofcoding9 {
    public static void main(String[] args) {
        cetakAngka();
    }

    // Fungsi untuk mencetak angka 1 hingga 10 dengan perulangan for
    static void cetakAngka() {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
