package LatihanBelajar1;

public class Main {
    
    public static void main(String[] args) {
        //buatlah perulangan dari 1-10 dan jumlahkan!.
        int hasil = 0;
        for(int i = 1; i <= 10; i++){
            System.out.println(i);
            hasil += i;
        }
        System.out.println("hasilnya ialah : "+hasil);
    }
    
}
